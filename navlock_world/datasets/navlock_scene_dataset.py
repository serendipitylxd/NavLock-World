"""Scene-level dataset for NavLock temporal world-model training.

The dataset consumes files generated by ``tools/build_navlock_sequences.py``.
It is intentionally lightweight: samples contain paths, timestamps, labels,
tracks, and masks. Image/point-cloud loading can be added in model-specific
pipelines without changing the sequence schema.
"""

from __future__ import annotations

import json
from copy import deepcopy
from pathlib import Path
from typing import Any, Callable, Iterable

try:  # PyTorch is optional in this repository environment.
    from torch.utils.data import Dataset as TorchDataset
except Exception:  # pragma: no cover - exercised when torch is absent.
    TorchDataset = object


LOCK_STATE_LABELS = {
    "open": 0,
    "closed": 1,
    "opening": 2,
    "closing": 3,
    "unknown": -1,
}

WATER_STATE_LABELS = {
    "idle": 0,
    "filling": 1,
    "emptying": 2,
    "unknown": -1,
}

SHIP_INTENTION_LABELS = {
    "ship_entering_lock": 0,
    "ship_leaving_lock": 1,
    "ship_berthed": 2,
}


class NavLockSceneDataset(TorchDataset):
    """Dataset over 60-second NavLock scenes.

    Args:
        data_root: Dataset root. Defaults to ``data``.
        split: One of ``train``, ``val``, ``test``, or ``all``.
        sequence_file: Optional explicit sequence JSON path.
        mode: ``recognition``, ``prediction``, or ``all``.
            ``recognition`` returns all scene frames for state/intention
            recognition.
            ``prediction`` returns input frames from the first 50 seconds and
            target frames from the last 10 seconds. Scenes without prediction
            targets are skipped by default.
            ``all`` returns all frames plus prediction indices when present.
        camera_channels: Optional camera subset. Defaults to all channels in
            metadata, including CAM_3 and CAM_8.
        require_prediction_target: If true, keeps only scenes with future
            target frames.
        root_paths: If true, image/lidar file names are expanded under
            ``data_root``.
        transform: Optional callable applied to the returned sample.
    """

    VALID_MODES = {"recognition", "prediction", "all"}

    def __init__(
        self,
        data_root: str | Path = "data",
        split: str = "train",
        sequence_file: str | Path | None = None,
        mode: str = "all",
        camera_channels: Iterable[str] | None = None,
        require_prediction_target: bool | None = None,
        root_paths: bool = True,
        transform: Callable[[dict[str, Any]], dict[str, Any]] | None = None,
    ) -> None:
        if mode not in self.VALID_MODES:
            raise ValueError(f"mode must be one of {sorted(self.VALID_MODES)}, got {mode!r}")

        self.data_root = Path(data_root)
        self.split = split
        self.mode = mode
        self.root_paths = root_paths
        self.transform = transform

        if sequence_file is None:
            sequence_file = self.data_root / "navlock_sequences" / f"scene_sequences_{split}.json"
        self.sequence_file = Path(sequence_file)

        with self.sequence_file.open("r", encoding="utf-8") as f:
            payload = json.load(f)

        self.metadata: dict[str, Any] = payload["metadata"]
        self.camera_channels = list(camera_channels or self.metadata["camera_channels"])
        missing_channels = set(self.camera_channels) - set(self.metadata["camera_channels"])
        if missing_channels:
            raise ValueError(f"unknown camera channels: {sorted(missing_channels)}")

        if require_prediction_target is None:
            require_prediction_target = mode == "prediction"
        self.require_prediction_target = require_prediction_target

        self.sequences = []
        for sequence in payload["sequences"]:
            if require_prediction_target and not sequence["has_prediction_target"]:
                continue
            self.sequences.append(sequence)

    def __len__(self) -> int:
        return len(self.sequences)

    def __getitem__(self, index: int) -> dict[str, Any]:
        sequence = self.sequences[index]
        sample = self._build_sample(sequence)
        if self.transform is not None:
            sample = self.transform(sample)
        return sample

    @property
    def label_maps(self) -> dict[str, dict[str, int]]:
        return {
            "upper_gate_state": LOCK_STATE_LABELS,
            "lower_gate_state": LOCK_STATE_LABELS,
            "water_state": WATER_STATE_LABELS,
            "ship_intention": SHIP_INTENTION_LABELS,
        }

    def _build_sample(self, sequence: dict[str, Any]) -> dict[str, Any]:
        frames = sequence["frames"]
        recognition_indices = sequence["recognition_frame_indices"]
        prediction_input_indices = sequence["prediction_input_frame_indices"]
        prediction_target_indices = sequence["prediction_target_frame_indices"]

        if self.mode == "recognition":
            active_indices = recognition_indices
        elif self.mode == "prediction":
            active_indices = prediction_input_indices
        else:
            active_indices = recognition_indices

        active_frames = [self._build_frame(frames[i]) for i in active_indices]
        target_frames = [self._build_frame(frames[i]) for i in prediction_target_indices]

        sample = {
            "scene_token": sequence["scene_token"],
            "scene_name": sequence["scene_name"],
            "split": sequence["split"],
            "direction": sequence.get("direction"),
            "operation_date": sequence.get("operation_date"),
            "operation_index": sequence.get("operation_index"),
            "segment_index": sequence.get("segment_index"),
            "mode": self.mode,
            "num_frames": len(active_frames),
            "has_prediction_target": sequence["has_prediction_target"],
            "frames": active_frames,
            "prediction": {
                "input_frame_indices": prediction_input_indices,
                "target_frame_indices": prediction_target_indices,
                "input_frames": [self._build_frame(frames[i]) for i in prediction_input_indices],
                "target_frames": target_frames,
                "has_target": sequence["has_prediction_target"],
            },
            "metadata": {
                "nominal_duration_sec": sequence["nominal_duration_sec"],
                "actual_duration_sec": sequence["actual_duration_sec"],
                "camera_channels": self.camera_channels,
                "geometric_camera_channels": [
                    ch for ch in sequence["geometric_camera_channels"] if ch in self.camera_channels
                ],
                "state_camera_channels": [
                    ch for ch in sequence["state_camera_channels"] if ch in self.camera_channels
                ],
                "lidar": self.metadata.get("lidar"),
                "label_schema": self.metadata.get("label_schema", {}),
            },
        }
        return sample

    def _build_frame(self, frame: dict[str, Any]) -> dict[str, Any]:
        images = {
            channel: self._with_root_path(deepcopy(frame["images"][channel]))
            for channel in self.camera_channels
        }
        lidar = deepcopy(frame["lidar"])
        if lidar is not None:
            lidar = self._with_root_path(lidar)

        lock_state = deepcopy(frame["lock_state"])
        encoded_lock_state = {
            "upper_gate_state": LOCK_STATE_LABELS.get(lock_state["upper_gate_state"], -1),
            "lower_gate_state": LOCK_STATE_LABELS.get(lock_state["lower_gate_state"], -1),
            "water_state": WATER_STATE_LABELS.get(lock_state["water_state"], -1),
        }
        water_level = lock_state.get("water_level")

        instances = []
        for instance in frame["instances_3d"]:
            item = deepcopy(instance)
            item["ship_intention_labels"] = [
                SHIP_INTENTION_LABELS[name]
                for name in item.get("ship_intentions", [])
                if name in SHIP_INTENTION_LABELS
            ]
            instances.append(item)

        return {
            "frame_index": frame["frame_index"],
            "sample_token": frame["sample_token"],
            "sample_idx": frame["sample_idx"],
            "frame_id": frame["frame_id"],
            "timestamp": frame["timestamp"],
            "timestamp_str": frame["timestamp_str"],
            "relative_time_sec": frame["relative_time_sec"],
            "images": images,
            "lidar": lidar,
            "lock_state": lock_state,
            "lock_state_labels": encoded_lock_state,
            "water_level": water_level,
            "instances_3d": instances,
        }

    def _with_root_path(self, item: dict[str, Any]) -> dict[str, Any]:
        if self.root_paths and item.get("file_name"):
            item["path"] = str(self.data_root / item["file_name"])
        return item


def navlock_scene_collate(batch: list[dict[str, Any]]) -> dict[str, Any]:
    """Collate variable-length scene samples without requiring torch.

    The function preserves each scene as a list of frame dictionaries and adds
    boolean masks for recognition and prediction lengths. Model-specific code
    can use these masks to build tensors after image/point-cloud loading.
    """

    max_frames = max((item["num_frames"] for item in batch), default=0)
    max_prediction_inputs = max(
        (len(item["prediction"]["input_frames"]) for item in batch), default=0
    )
    max_prediction_targets = max(
        (len(item["prediction"]["target_frames"]) for item in batch), default=0
    )

    return {
        "scene_tokens": [item["scene_token"] for item in batch],
        "scene_names": [item["scene_name"] for item in batch],
        "splits": [item["split"] for item in batch],
        "directions": [item["direction"] for item in batch],
        "has_prediction_target": [item["has_prediction_target"] for item in batch],
        "frames": [item["frames"] for item in batch],
        "frame_masks": [
            [i < item["num_frames"] for i in range(max_frames)] for item in batch
        ],
        "prediction_input_frames": [
            item["prediction"]["input_frames"] for item in batch
        ],
        "prediction_input_masks": [
            [i < len(item["prediction"]["input_frames"]) for i in range(max_prediction_inputs)]
            for item in batch
        ],
        "prediction_target_frames": [
            item["prediction"]["target_frames"] for item in batch
        ],
        "prediction_target_masks": [
            [i < len(item["prediction"]["target_frames"]) for i in range(max_prediction_targets)]
            for item in batch
        ],
        "metadata": [item["metadata"] for item in batch],
    }
